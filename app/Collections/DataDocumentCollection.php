<?php

namespace Web2CV\Collections;

class DataDocumentCollection implements \ArrayAccess
{
    /**
	 * @var array $dataDocuments
	 */
	protected $dataDocuments;

    /**
     * Constructor
     */
    public function __construct()
    {
        $this->dataDocuments = array();
    }

    /**
     * Return the data in array form
     *
     * @return array
     */
    public function toArray()
    {
        $dataDocumentsArray = array();
        foreach ($this->dataDocuments as $documentName => $dataDocument){
            $dataDocumentsArray[$documentName] = $dataDocument->toArray();
        }
        return $dataDocumentsArray;
    }

    /**
     * (PHP 5 &gt;= 5.0.0)<br/>
     * Whether a offset exists
     * @link http://php.net/manual/en/arrayaccess.offsetexists.php
     * @param mixed $offset <p>
     * An offset to check for.
     * </p>
     * @return boolean true on success or false on failure.
     * </p>
     * <p>
     * The return value will be casted to boolean if non-boolean was returned.
     */
    public function offsetExists($offset)
    {
        return isset($this->dataDocuments[$offset]);
    }

    /**
     * (PHP 5 &gt;= 5.0.0)<br/>
     * Offset to retrieve
     * @link http://php.net/manual/en/arrayaccess.offsetget.php
     * @param mixed $offset <p>
     * The offset to retrieve.
     * </p>
     * @return mixed Can return all value types.
     */
    public function offsetGet($offset)
    {
        return isset($this->dataDocuments[$offset]) ? $this->dataDocuments[$offset] : null;
    }

    /**
     * (PHP 5 &gt;= 5.0.0)<br/>
     * Offset to set
     * @link http://php.net/manual/en/arrayaccess.offsetset.php
     * @param mixed $offset <p>
     * The offset to assign the value to.
     * </p>
     * @param mixed $value <p>
     * The value to set.
     * </p>
     * @return void
     */
    public function offsetSet($offset, $value)
    {
        if (is_null($offset)) {
            $this->dataDocuments[] = $value;
        } else {
            $this->dataDocuments[$offset] = $value;
        }
    }

    /**
     * (PHP 5 &gt;= 5.0.0)<br/>
     * Offset to unset
     * @link http://php.net/manual/en/arrayaccess.offsetunset.php
     * @param mixed $offset <p>
     * The offset to unset.
     * </p>
     * @return void
     */
    public function offsetUnset($offset)
    {
        unset($this->dataDocuments[$offset]);
    }
}
